z = ('Car', 'Bike', 'Jeep')

# Tuples are ordered, unchangeable and allow duplicates
print(z)

print(len(z))

print(type(z))

# To convert a tuple to list

y = list(z)

print(type(y))

y.append("Last object")

# To change a list to tuple

x = tuple(y)

print(x)

now_a_tuple = ('cat',)

print(type(now_a_tuple))
